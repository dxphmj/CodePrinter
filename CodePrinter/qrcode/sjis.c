/*
 * JISX0201.1976-0 (libiconv-1.16/lib/jisx0201.h)
 */

static int jisx0201_wctomb(unsigned int* r, unsigned int wc) {
    if (wc < 0x0080 && !(wc == 0x005c || wc == 0x007e)) {
        *r = wc;
        return 1;
    }
    if (wc == 0x00a5) {
        *r = 0x5c;
        return 1;
    }
    if (wc == 0x203e) {
        *r = 0x7e;
        return 1;
    }
    if (wc >= 0xff61 && wc < 0xffa0) {
        *r = wc - 0xfec0;
        return 1;
    }
    return 0;
}

